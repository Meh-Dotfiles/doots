#!/bin/sh

function get {

	export $1=`echo "$GTKRC" | grep $2 | cut -d ' ' -f 4 | tr -d '\"'`
	if [ $$1 ]; then return; fi

	echo "Theme '$THEME' is not a valid theme"
	exit 1

}

if [ $# -eq 0 -o "$1" = "-h" -o "$1" = --help ]; then

	echo Usage: gtktoi3.sh [options] [theme]
	echo Append output to i3 config to match GTK theme colors
	echo
	echo Options:
	echo "	-h, --help	display this message"
	echo "	-l, --list	list themes"
	echo "	-a, --auto	auto-detect theme"

	exit 1

fi

if [ $1 = "-l" -o $1 = "--list" ]; then

	echo Themes:
	echo
	ls /usr/share/themes/*/gtk-2.0/gtkrc | cut -d '/' -f 5
	exit 0

fi

if [ $1 = "-a" -o $1 = "--auto" ]

then
	if [ -f ~/.gtkrc-2.0 ]

	then
		THEME=`grep < ~/.gtkrc-2.0 gtk-theme-name | cut -d ' ' -f 3 | tr -d \"`

	else
		echo Could not determine theme

	fi

else
	THEME="$1"

fi

if [ -d /usr/share/themes/$THEME ]

then
	GTKRC=`cat /usr/share/themes/$THEME/gtk-2.0/gtkrc 2> /dev/null`
	if [ $? -ne 0 ]; then

		echo "Theme '$THEME' is not a valid theme"
		exit 1

	fi
else
	echo "Theme '$THEME' does not exist"
	exit 1

fi

get BORDER insensitive_bg_color
get TEXT text_color
get INDICATOR selected_bg_color
get IBORDER \"bg_color
URGENT=\#900000

echo   "# i3 colors from GTK2+ theme '$THEME' generated by gtktoi3.sh"
echo   "# class					title separator		title		text		indicator	client border
		client.focused			$BORDER				$BORDER		$TEXT		$INDICATOR	$BORDER
		client.focused_inactive	$IBORDER			$IBORDER	$TEXT		$INDICATOR	$IBORDER
		client.unfocused		$IBORDER			$IBORDER	$TEXT		$INDICATOR	$IBORDER
		client.urgent			$URGENT				$URGENT		$TEXT		$INDICATOR	$URGENT
		client.placeholder		$BORDER				$BORDER		$TEXT		$INDICATOR	$BORDER
		client.background		$BORDER" | tr -s '\t' | column -t -s $'\t'
